uint16_t  insert_best(uint16_t best_distances[], uint16_t best_datapoints[],uint16_t distance,uint16_t no)
{
  //best_*[0] :besta
  //best_*[KNN_K-1] : worst in list
  
  //get position where to move the new point
  uint16_t i;
  uint16_t pos,tomove_dist,tomove_no,temp;
  for (i=KNN_K-1; i>=0; i--)
  {
    if(best_distances[i]<=distance) break;
  }
  
  //insert the new point
  pos=++i;
  tomove_dist=distance;
  tomove_no=no;
  for (i=pos; i<KNN_K; i++)
  {
    temp=best_distances[i];
    best_distances[i]=tomove_dist;
    tomove_dist=temp;
    temp=best_datapoints[i];
    best_datapoints[i]=tomove_no;
    tomove_no=temp;
   }
  return 0;
}

uint16_t  classify_knn(uint16_t features[])
{
  uint16_t number_of_instances[KNN_NUMOFCLASSES];
  uint16_t class_no,max;
  uint16_t distance,i,j;
  uint16_t best_distances[KNN_K];
  uint16_t best_datapoints[KNN_K];
  for (i=0; i<KNN_K;i++) {
  best_distances[i]=4000;    //1<<16-1
  best_datapoints[i]=33;
  }
 
  for (i=0; i<KNN_NUMOFPOINTS;i++)
  {
   distance=0;
   
   for (j=0 ;j<KNN_NUMOFFEATURES*KNN_NUMOFSENSORS;j++)
   {
     if (distance>best_distances[KNN_K-1]) break; //to make it faster;
     distance+=abs(features[j]-knn_datapoints[i][j])>>2;
   }
   
   //distance++; //why??
   if(distance<best_distances[KNN_K-1])
   insert_best(best_distances,best_datapoints,distance,i);
       
  }
  
  
  
  //initialize
  for (i=0;i<KNN_NUMOFCLASSES;i++) number_of_instances[i]=0;
    
  //fill
  for (i=0;i<KNN_K;i++)
  {
   class_no =knn_datapoints[best_datapoints[i]][KNN_NUMOFFEATURES*KNN_NUMOFSENSORS];
   number_of_instances[class_no]++; 
   
  }
  
  //get class with the maximum of instances in the k-neighborhood
    max=0;
  for (i=0;i<KNN_NUMOFCLASSES;i++) 
  {
    if (number_of_instances[i]>max) {max=number_of_instances[i]; class_no=i;}
  }
  return class_no;
}




